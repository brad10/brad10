Mercredi 18 mars, le gouvernement péruvien a décrété l’Etat d’Urgence National comme mesure de prévention et de contrôle du Covid-19 et ce durant 15 jours. <br><br>
Le Président des Etats-Unis, Donald Trump, l’a annoncé mercredi, la frontière entre les Etats-Unis et le Canada sera temporairement fermée. <br><br>
Aux Etats-Unis où l'état d'urgence a été déclaré mais où les mesures sont à la discrétion des états, les décisions sont de plus en plus drastiques. Exemple : dans le New-Jersey, un couvre-feu est décrété à partir de ce lundi sur les commerces et déplacements non essentiels. <br><br>
Plusieurs pays d’Amérique latine dont le Venezuela, l’Equateur, la Bolivie et le Guatemala ont renforcé les mesures de restriction à l’égard des personnes en provenance des pays les plus touchés, dont la France. <br><br>
Aux États-Unis 10 346 cas ont été recensés par les autorités sanitaires pour 155 mortels ce jeudi 19 mars. Mais il se pourrait que le nombre de personnes contaminées soit bien plus important. De nombreux américains n'effectuent pas de test de dépistage car ceux-ci sont payants dans ce pays où la santé est une affaire de moyens financiers personnels. <br><br>