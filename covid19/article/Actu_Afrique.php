
<h3>Afrique</h3>
Mardi 17 au soir voici le bilan des pays africains (nombre de cas / nombre de décès) les plus touchés par l'épidémie : <br><br>

<b>Afrique du Sud :</b>  62 ; <br> 
<b>Algérie :</b>  60 / 4 ; <br>
<b>Cameroun :</b>   17 / 0 ; <br>
<b>Egypte :</b>  166 / 4 ; <br>
<b>Gabon Kenya Maroc :</b>  38 / 2 ; <br> 
<b>Sénégal :</b>  26 / 2 ;<br>
<b>Tunisie :</b>  24 <br><br>

Le continent ne compte "que" 18 morts liées au coronavirus. <br><br>

Le bilan africain ne laisse pas de surprendre. Comme le signalait le journal Les Echos ce mardi 17, "'Afrique compte officiellement cent fois moins de cas de Covid19 que l'Italie, pourtant vingt fois moins peuplée. C'est en fait le seul continent quasiment indemne, bien moins touché encore que l'Amérique latine ou l'Océanie, les deux autres grandes régions relativement préservées." <br><br>

Ce mercredi 18, l’Algérie interdit les rassemblements et les marches pour lutter contre la propagation de l’épidémie