<p>Le Premier ministre camerounais Joseph Dion Ngute a annoncé mardi une série de mesures face à la pandémie de coronavirus.
</p>

Les frontières terrestres, aériennes et maritimes du Cameroun seront fermées : <br><br>
<li>tous les vols passagers en provenance de l’étranger sont suspendus, à l’exception des vols cargos et des navires transportant les produits de consommation courante ainsi que les biens et matériels essentiels, dont les temps d’escale seront limités et encadrés ; les camerounais désireux de retourner dans leur pays devront prendre l’attache des différentes représentations diplomatiques.
</li><br>
<li>La délivrance des visas d’entrée au Cameroun aux différents aéroports est suspendue.
</li><br>
<li>Tous les établissements publics et privés de formation relevant des différents ordres d’enseignement, de la maternelle au supérieur, y compris les centres de formation professionnelle et les grandes écoles seront fermés.
</li><br>
<li>Les rassemblements de plus de cinquante (50) personnes sont interdits sur toute l’étendue du territoire national.
</li><br>
<li>Les compétitions scolaires et universitaires sont reportées, à l’instar des jeux FENASSCO et des jeux universitaires.
</li><br>
<li>Les débits de boissons, les restaurants et les lieux de loisirs seront systématiquement fermés à partir de 18 heures, sous le contrôle des autorités administratives.
</li><br>
<li>Un système de régulation des flux des consommateurs sera instauré dans les marchés et les centres commerciaux. 
</li><br>
<li>Les déplacements urbains et interurbains ne devront s’effectuer qu’en cas d’extrême nécessité.
</li><br>
<li>Les conducteurs de bus, de taxis et de mototaxis sont invités à éviter des surcharges dans les transports publics : les forces de maintien de l’ordre y veilleront particulièrement.
</li><br>
<li>Les formations sanitaires privées, les hôtels et autres lieux d’hébergement, les véhicules ainsi que les équipements spécifiques nécessaires à la mise en œuvre du plan de riposte contre la pandémie du COVID-19 au Cameroun pourront être réquisitionnées en tant que de besoin, à la diligence des autorités compétentes.
</li><br>
<li>Les administrations publiques devront privilégier les moyens de communications électroniques et les outils numériques pour les réunions susceptibles de regrouper plus de dix (10) personnes.
</li><br>
<li>Les missions à l’étranger des membres du Gouvernement et des agents du secteur public et parapublic sont suspendues ; Il s’agit des mesures certes difficiles mais nécessaires pour garantir la protection de tous et de chacun et limiter la propagation de cette pandémie.
</li><br>
<li>Les populations sont invitées à observer strictement les mesures d’hygiène recommandées par l’Organisation Mondiale de la Santé, à savoir notamment se laver régulièrement les mains au savon, éviter des contacts rapprochés tels que se serrer les mains ou s'embrasser.
</li><br>
<li>En cas de nécessité, les populations sont invitées à appeler le numéro vert 1510 mis en place pour la mobilisation des équipes de secours.
</li><br>
<li>Le Gouvernement invite les populations à ne pas céder à la panique, mais à faire preuve de discipline, de solidarité et de sens des responsabilités, au moment où le monde entier traverse des moments difficiles.</li>
