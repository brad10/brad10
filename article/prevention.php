<h3>Quels sont les mesures de prevention contre le covid 19</h3><br>
Il n'existe actuellement aucun vaccin contre la maladie à coronavirus 2019 (COVID-19). <br><br>
<b>Vous pouvez réduire le risque de contracter l'infection si :</b>
 <br><br>
<li>vous vous nettoyez fréquemment les mains avec un produit hydroalcoolique ou à l'eau et au savon
</li>
<img src="media/1387114364.jpg" alt=""><br><br>
<li>vous vous couvrez le nez et la bouche avec un mouchoir ou le creux du coude quand vous toussez ou éternuez
</li>
<img src="media/unnamed.jpg" alt=""><br><br>
<li>vous évitez les contacts étroits (à moins de 1 mètre ou 3 pieds) avec toute personne ayant des symptômes de rhume banal ou d'état grippal</li>
